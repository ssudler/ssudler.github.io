function draw(pos) {
  for (const player in data) {
    const newrgb = data[player].rgb.replace('"', '').replace('(', '').replace(')', '');
    const color = scale(data[player].avgcolor, 0, 765, 0, 100);
    const salary = scale(parseInt(data[player].salary), 0, 40000000, 0, 300);

    const div = document.createElement('div');
    div.setAttribute('class', 'player-class');
    div.style.height = '20px';
    div.style.width = '20px';
    div.style.position = 'absolute';
    div.style.top = `${1.4 * player}px`;
    div.style.background = `rgb(${newrgb})`;
    div.style.left = `${pos ? color : salary}%`;
    div.style.borderRadius = '20px';
    div.style.marginRight = '20px';

    container.appendChild(div);
  }
}

function toggleColor() {
  container.innerHTML = "";
  stickyBottom.style.backgroundImage = 'linear-gradient(to right, #5f4121, white)';
  leftLabel.innerHTML = 'Darker';
  leftLabel.style.color = 'white';
  rightLabel.innerHTML = 'Lighter';
  rightLabel.style.color = 'black';
  draw(true);
};

function toggleSalary() {
  container.innerHTML = "";
  stickyBottom.style.backgroundImage = 'linear-gradient(to right, white, green)';
  rightLabel.innerHTML = 'Higher Salary';
  rightLabel.style.color = 'white';
  leftLabel.innerHTML = 'Lower Salary';
  leftLabel.style.color = 'black';
  draw(false);
};

